import React from 'react';
import { FaCheckCircle, FaTimesCircle, FaChartLine, FaTrophy, FaDownload } from 'react-icons/fa';

const Results = ({ data, onReset }) => {
  if (!data) return null;

  const getScoreColor = (score) => {
    if (score >= 70) return 'text-green-500';
    if (score >= 40) return 'text-yellow-500';
    return 'text-red-500';
  };

  const getScoreBgColor = (score) => {
    if (score >= 70) return 'bg-green-500';
    if (score >= 40) return 'bg-yellow-500';
    return 'bg-red-500';
  };

  const getScoreLabel = (score) => {
    if (score >= 70) return 'Excellent Match';
    if (score >= 40) return 'Good Match';
    return 'Fair Match';
  };

  const downloadResults = () => {
    const resultsText = `SKILLLINK RESUME ANALYSIS RESULTS
=====================================

YOUR SKILLS (${data.totalSkillsFound} found):
${data.userSkills.join(', ')}

JOB MATCHES:
${data.matches.map((match, index) => `
${index + 1}. ${match.jobTitle}
   Match Score: ${match.score}%
   Required Skills: ${match.requiredSkills.join(', ')}
   Your Matched Skills: ${match.matchedSkills.join(', ') || 'None'}
   Skills to Learn: ${match.missingSkills.join(', ') || 'None'}
`).join('\n')}

Generated by SkillLink - AI Resume Analyzer`;

    const blob = new Blob([resultsText], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'skilllink-analysis-results.txt';
    a.click();
    URL.revokeObjectURL(url);
  };

  return (
    <div className="max-w-6xl mx-auto animate-fade-in">
      {/* Action Buttons */}
      <div className="mb-6 flex justify-between items-center">
        <button
          onClick={onReset}
          className="bg-gray-600 text-white px-4 py-2 rounded-lg hover:bg-gray-700 transition-colors"
        >
          ‚Üê Analyze Another Resume
        </button>
        <button
          onClick={downloadResults}
          className="bg-blue-600 text-white px-4 py-2 rounded-lg hover:bg-blue-700 transition-colors flex items-center"
        >
          <FaDownload className="mr-2" />
          Download Results
        </button>
      </div>

      {/* Skills Summary */}
      <div className="bg-white rounded-lg shadow-lg p-6 mb-6">
        <h3 className="text-xl font-bold mb-4 flex items-center">
          <FaChartLine className="mr-2 text-blue-600" />
          Your Skills Summary
        </h3>
        <div className="flex flex-wrap gap-2 mb-4">
          {data.userSkills.map((skill, index) => (
            <span
              key={index}
              className="px-3 py-1 bg-blue-100 text-blue-700 rounded-full text-sm font-medium hover:bg-blue-200 transition-colors"
            >
              {skill}
            </span>
          ))}
        </div>
        <p className="text-gray-600">
          Total skills identified: <span className="font-bold text-blue-600 text-lg">{data.totalSkillsFound}</span>
        </p>
      </div>

      {/* Best Match Alert */}
      {data.matches.length > 0 && data.matches[0].score >= 70 && (
        <div className="bg-gradient-to-r from-green-50 to-emerald-50 border-2 border-green-300 rounded-lg p-6 mb-6 animate-bounce-light">
          <div className="flex items-center mb-2">
            <FaTrophy className="text-3xl text-yellow-500 mr-3" />
            <h3 className="text-xl font-bold text-green-800">Excellent Match Found!</h3>
          </div>
          <p className="text-green-700">
            You're a great fit for <span className="font-bold">{data.matches[0].jobTitle}</span> with 
            a <span className="font-bold text-lg">{data.matches[0].score}%</span> match!
          </p>
        </div>
      )}

      {/* Job Matches */}
      <h3 className="text-2xl font-bold mb-4">Job Matches</h3>
      
      {data.matches.length === 0 ? (
        <div className="bg-white rounded-lg shadow-lg p-6 text-center">
          <p className="text-gray-600 text-lg">No matching jobs found. Try adding more skills to your resume!</p>
        </div>
      ) : (
        data.matches.map((match, index) => (
          <div key={index} className="bg-white rounded-lg shadow-lg p-6 mb-4 hover:shadow-xl transition-shadow">
            <div className="flex justify-between items-start mb-4">
              <div className="flex-1">
                <h4 className="text-xl font-bold mb-1">
                  {index + 1}. {match.jobTitle}
                </h4>
                <p className="text-gray-600">{match.jobDescription}</p>
              </div>
              <div className="text-center ml-4">
                <div className={`text-3xl font-bold ${getScoreColor(match.score)}`}>
                  {match.score}%
                </div>
                <p className={`text-sm font-medium ${getScoreColor(match.score)}`}>
                  {getScoreLabel(match.score)}
                </p>
              </div>
            </div>

            {/* Progress Bar */}
            <div className="w-full bg-gray-200 rounded-full h-3 mb-4 overflow-hidden">
              <div
                className={`h-3 rounded-full transition-all duration-1000 ${getScoreBgColor(match.score)}`}
                style={{ width: `${match.score}%` }}
              />
            </div>

            {/* Skills Details */}
            <div className="grid md:grid-cols-2 gap-4">
              {/* Matched Skills */}
              <div className="bg-green-50 p-4 rounded-lg">
                <h5 className="font-semibold mb-2 flex items-center text-green-700">
                  <FaCheckCircle className="mr-2" />
                  Matched Skills ({match.totalMatched}/{match.totalRequired})
                </h5>
                <div className="flex flex-wrap gap-1">
                  {match.matchedSkills.length > 0 ? (
                    match.matchedSkills.map((skill, idx) => (
                      <span
                        key={idx}
                        className="px-2 py-1 bg-green-100 text-green-700 rounded text-sm font-medium"
                      >
                        {skill}
                      </span>
                    ))
                  ) : (
                    <span className="text-gray-500 text-sm italic">No matching skills</span>
                  )}
                </div>
              </div>

              {/* Missing Skills */}
              <div className="bg-red-50 p-4 rounded-lg">
                <h5 className="font-semibold mb-2 flex items-center text-red-700">
                  <FaTimesCircle className="mr-2" />
                  Skills to Learn
                </h5>
                <div className="flex flex-wrap gap-1">
                  {match.missingSkills.length > 0 ? (
                    match.missingSkills.map((skill, idx) => (
                      <span
                        key={idx}
                        className="px-2 py-1 bg-red-100 text-red-700 rounded text-sm font-medium"
                      >
                        {skill}
                      </span>
                    ))
                  ) : (
                    <span className="text-gray-500 text-sm italic">You have all required skills!</span>
                  )}
                </div>
              </div>
            </div>

            {/* Required Skills List */}
            <div className="mt-4 pt-4 border-t border-gray-200">
              <p className="text-sm text-gray-600">
                <strong>All Required Skills:</strong> {match.requiredSkills.join(', ')}
              </p>
            </div>
          </div>
        ))
      )}

      {/* Summary Stats */}
      <div className="mt-8 bg-gray-100 rounded-lg p-6">
        <h4 className="font-bold mb-3">Analysis Summary</h4>
        <div className="grid grid-cols-2 md:grid-cols-4 gap-4 text-center">
          <div>
            <p className="text-2xl font-bold text-blue-600">{data.totalSkillsFound}</p>
            <p className="text-sm text-gray-600">Skills Found</p>
          </div>
          <div>
            <p className="text-2xl font-bold text-green-600">
              {data.matches.filter(m => m.score >= 70).length}
            </p>
            <p className="text-sm text-gray-600">Excellent Matches</p>
          </div>
          <div>
            <p className="text-2xl font-bold text-yellow-600">
              {data.matches.filter(m => m.score >= 40 && m.score < 70).length}
            </p>
            <p className="text-sm text-gray-600">Good Matches</p>
          </div>
          <div>
            <p className="text-2xl font-bold text-gray-600">
              {data.matches.length}
            </p>
            <p className="text-sm text-gray-600">Total Matches</p>
          </div>
        </div>
      </div>
    </div>
  );
};

export default Results;
